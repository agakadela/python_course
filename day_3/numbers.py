# Numbers in python are integers and floats
# Integers are whole numbers
# Floats are numbers with decimals
# How to check the type of the variable?
# type() function
#
print(type(1))
print(type(1.0))

# How to convert one data type to another?
# int() function
# float() function
print(int(1.0))
print(float(1))

# Operations on numbers
# Addition
print(1 + 1)
# Subtraction
print(1 - 1)
# Multiplication
print(1 * 1)
# Division
print(4 / 2)
# Floor division
print(15 // 2)
# Modulo
print(10 % 2)
# Multiplication - power of the number (2 ** 3 = 2 * 2 * 2)
print(2 ** 3)

# Operator precedence
# 1. ()
# 2. **
# 3. * / // %
# 4. + -
print((5 + 4) * 10 / 2)
